function changeLanguage(lang) {
  // Save preference
  localStorage.setItem("preferredLanguage", lang)

  // Update button text
  const currentLangBtn = document.getElementById("currentLang")
  if (currentLangBtn) {
    currentLangBtn.textContent = `ðŸŒ ${lang.toUpperCase()}`
  }

  // Apply translations
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n")
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translations[lang][key]
      } else {
        element.textContent = translations[lang][key]
      }
    }
  })

  // Update HTML lang and dir attributes
  document.documentElement.lang = lang
  if (lang === "ar") {
    document.documentElement.dir = "rtl"
  } else {
    document.documentElement.dir = "ltr"
  }

  // Update page title
  document.title = translations[lang].page_title || document.title

  // Dispatch custom event for greeting update
  document.dispatchEvent(new Event("languageChanged"))
}

// Load saved language preference on page load
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("preferredLanguage") || "sq"
  changeLanguage(savedLang)
})

// Language switcher functionality
const currentLangBtn = document.getElementById("currentLang")
const langDropdown = document.getElementById("langDropdown")

if (currentLangBtn && langDropdown) {
  currentLangBtn.addEventListener("click", () => {
    langDropdown.classList.toggle("active")
  })

  // Close dropdown when clicking outside
  document.addEventListener("click", (e) => {
    if (!e.target.closest(".language-selector")) {
      langDropdown.classList.remove("active")
    }
  })

  // Language selection
  langDropdown.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.getAttribute("data-lang")
      changeLanguage(lang)
      langDropdown.classList.remove("active")
    })
  })
}
